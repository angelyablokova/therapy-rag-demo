triggers = {
    "—Å—É–∏—Ü–∏–¥": "‚ÄºÔ∏è –û–±–Ω–∞—Ä—É–∂–µ–Ω —Ä–∏—Å–∫: –Ω–µ–º–µ–¥–ª–µ–Ω–Ω–æ –æ–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∑–∞ –ø–æ–º–æ—â—å—é.",
    "–¥–µ–ø—Ä–µ—Å—Å–∏—è": "üìâ –°–∏–º–ø—Ç–æ–º—ã –¥–µ–ø—Ä–µ—Å—Å–∏–∏. –†–µ–∫–æ–º–µ–Ω–¥—É–µ—Ç—Å—è —Å–∞–º–æ–Ω–∞–±–ª—é–¥–µ–Ω–∏–µ –∏ –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏—è.",
    "—Ç—Ä–µ–≤–æ–≥–∞": "‚ö†Ô∏è –û—Ç–º–µ—á–∞–µ—Ç—Å—è —Ç—Ä–µ–≤–æ–∂–Ω–æ—Å—Ç—å ‚Äî –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –∏–Ω—Ç–µ–Ω—Å–∏–≤–Ω–æ—Å—Ç—å –∏ –ø—Ä–æ–¥–æ–ª–∂–∏—Ç–µ–ª—å–Ω–æ—Å—Ç—å."
}

# 2. –§—É–Ω–∫—Ü–∏—è –∞–Ω–∞–ª–∏–∑–∞, –≤–æ–∑–≤—Ä–∞—â–∞—é—â–∞—è —Å–ø–∏—Å–æ–∫ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö —Ä–∏—Å–∫–æ–≤
def analyze_response(text):
    text_lower = text.lower()
    detected = []

    for keyword, message in triggers.items():
        if keyword in text_lower:
            detected.append((keyword, message))

    return detected

# 3. –ü–æ–ª—É—á–µ–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞ –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
